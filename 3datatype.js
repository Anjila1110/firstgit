//3.Define variables with different data types (string, number, boolean, object, undefined, null) and log their types using typeof.

//defining variables with different datatypes
let myString = "Anjila";                    // string
let myNumber = 1110;                         // number
let myBoolean = true;                       // boolean
let myObject = { name: "Anji", age: 20 };   // object
let myUndefined;                            // undefined
let myNull = null;                          // null

// Logging the types of the variables using typeof
console.log(typeof myString);        // "string"
console.log(typeof myNumber);        // "number"
console.log(typeof myBoolean);       // "boolean"
console.log(typeof myObject);        // "object"
console.log(typeof myUndefined);    // "undefined"